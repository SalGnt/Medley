package medley.tone;

/**
 * {@code Name} class that represents musical notes name, represented using the
 * English literal notation.
 */
public enum Name {

    /**
     * C, or Do, is the first note of the <i>fixed-Do solfege</i> system.
     */
    C,

    /**
     * D, or Re, is the second note of the <i>fixed-Do solfege</i> system.
     */
    D,

    /**
     * E, or Mi, is the third note of the <i>fixed-Do solfege</i> system.
     */
    E,

    /**
     * F, or Fa, is the fourth note of the <i>fixed-Do solfege</i> system.
     */
    F,

    /**
     * G, or Sol, is the fifth note of the <i>fixed-Do solfege</i> system.
     */
    G,

    /**
     * A, or La, is the sixth note of the <i>fixed-Do solfege</i> system.
     */
    A,

    /**
     * B, or Si, is the seventh note of the <i>fixed-Do solfege</i> system.
     */
    B;

};
